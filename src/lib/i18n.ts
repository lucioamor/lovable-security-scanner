export type Locale = 'en' | 'pt' | 'es';

let currentLocale: Locale = 'en';

export function setLocale(locale: Locale) {
  currentLocale = locale;
  try { localStorage.setItem('lss_locale', locale); } catch { /* noop */ }
}

export function getLocale(): Locale {
  return currentLocale;
}

export function loadLocale() {
  try {
    const saved = localStorage.getItem('lss_locale') as Locale | null;
    if (saved && ['en', 'pt', 'es'].includes(saved)) {
      currentLocale = saved;
    }
  } catch { /* noop */ }
}

export function t(key: string): string {
  const dict = TRANSLATIONS[currentLocale] || TRANSLATIONS.en;
  return (dict as Record<string, string>)[key] ?? (TRANSLATIONS.en as Record<string, string>)[key] ?? key;
}

